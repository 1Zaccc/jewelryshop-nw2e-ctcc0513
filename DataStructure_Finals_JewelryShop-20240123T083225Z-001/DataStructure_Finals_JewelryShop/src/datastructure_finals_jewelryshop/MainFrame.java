    private void paybtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_paybtnActionPerformed
        int numrow = ordertable.getRowCount();

        double tot = 0;

        for(int i=0;i<numrow;i++){
            double ftot = Double.valueOf(ordertable.getValueAt(i,2).toString());
            tot += ftot;
        }
        double bayad = Double.parseDouble(pay.getText());
        double sukli = bayad - tot;

        change.setText(Double.toString(sukli));;

        //for bill
        textarea.setText(textarea.getText() + "-------------------------------------------------------\n");
        textarea.setText(textarea.getText() + "-----------------Jewelry Shop-----------------\n");
        textarea.setText(textarea.getText() + "-------------------------------------------------------\n");
        textarea.setText(textarea.getText() + "ID" + "\t" + "NAME" + "\t" + "PRICE" + "\n");
        DefaultTableModel order = (DefaultTableModel) ordertable.getModel();
        for (int i=0; i<ordertable.getRowCount(); i++){
            String id = ordertable.getValueAt(i, 0).toString();
            String name = ordertable.getValueAt(i, 1).toString();
            String price = ordertable.getValueAt(i, 2).toString();
            textarea.setText(textarea.getText() + id + "\t" + name + "\t" + price + "\n");
        }
        textarea.setText(textarea.getText() + "Total purchase" + "\t" + "\t" +  tot + "\n");
        textarea.setText(textarea.getText() + "Total payment" + "\t" + "\t" +  bayad + "\n");
        textarea.setText(textarea.getText() + "Total change" + "\t" + "\t" +  sukli + "\n");
        textarea.setText(textarea.getText() + "-------------------------------------------------------\n");
        textarea.setText(textarea.getText() + "-----------------Jewelry Shop-----------------\n");
        textarea.setText(textarea.getText() + "-------------------------------------------------------\n");
    }//GEN-LAST:event_paybtnActionPerformed

