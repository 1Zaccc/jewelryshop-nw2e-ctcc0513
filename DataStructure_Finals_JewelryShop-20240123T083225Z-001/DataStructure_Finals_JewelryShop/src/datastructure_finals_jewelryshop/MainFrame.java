    public class items {
        public String id;
        public String name;
        public String price;
        
        public items(String id, String name, String price){
            this.id = id;
            this.name = name;
            this.price = price;
        }
    }
    public ArrayList listtable(){
        ArrayList<items> list = new ArrayList<items>();
        items i1 = new items("001","ring","1500");
        items i2 = new items("002","necklace","3000");
        items i3 = new items("003","earring","1000");
        items i4 = new items("004","bracelet","2000");
        items i5 = new items("005","engagementring","3500");
        list.add(i1);
        list.add(i2);
        list.add(i3);
        list.add(i4);
        list.add(i5);
        return list;
    }
    public void addRowTolisttable()
    {
        DefaultTableModel table = (DefaultTableModel) listtable.getModel();
        ArrayList<items> list = listtable();
        Object rowData[] = new Object[4];
        for(int i = 0; i < list.size(); i++)
        {
            rowData[0] = list.get(i).id;
            rowData[1] = list.get(i).name;
            rowData[2] = list.get(i).price;
            table.addRow(rowData);
        }
                
    }

