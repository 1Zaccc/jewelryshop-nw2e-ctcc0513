    private void addbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addbtnActionPerformed
        DefaultTableModel order = (DefaultTableModel) ordertable.getModel();
        DefaultTableModel item = (DefaultTableModel) listtable.getModel();
        int [] indexs = listtable.getSelectedRows();
        Object [] row = new Object[3];

        for(int i=0; i<indexs.length; i++){
            row[0] = item.getValueAt(indexs[i], 0);
            row[1] = item.getValueAt(indexs[i], 1);
            row[2] = item.getValueAt(indexs[i], 2);
            order.addRow(row);
        }
    }//GEN-LAST:event_addbtnActionPerformed

